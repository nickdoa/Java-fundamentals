public class ASCIIArt {
  public static void main(String[] args) {
    System.out.println("     __//     ");
    System.out.println("nc  /.__.\\ ");
    System.out.println("    \\ \\/ /");
    System.out.println(" '__/    \\     ");
    System.out.println("  \\-      )"); 
    System.out.println("   \\_____/");
    System.out.println("     | |");
    System.out.println("_____ _ _____");
    String doubleQuotes = "     \" \" ";
    System.out.println(doubleQuotes);
  }
}


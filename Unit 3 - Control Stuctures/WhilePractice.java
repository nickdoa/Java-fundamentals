public class WhilePractice {
    public static void main(String[] args) {
        int i = 1;
        while (i <= 25) {
            System.out.print(i + " ");
            i++;
        }
        System.out.println(); 
        System.out.println(" ");

        i = 50;
        while (i <= 70) {
            System.out.print(i + " ");
            i++;
        }
        System.out.println(); 
        System.out.println(" ");

        i = 0;
        while (i <= 100) {
            System.out.print(i + " ");
            i += 5;
        }
        System.out.println(); 
        System.out.println(" ");
        
        i = 20;
        while (i >= 1) {
            System.out.print(i + " ");
            i--;
        }
        System.out.println(); 
        System.out.println(" ");

        i = 1;
        while (i <= 15) {
            System.out.println("computer");
            i++;
        }
    }
}

